import re
import unicodedata

# Стоп-слова, которые не несут смысловой нагрузки при сопоставлении
_STOP_WORDS = {
    "шт", "штук", "уп", "упак", "упаковка", "упаковок",
    "г", "гр", "грамм", "кг", "кило", "килограмм",
    "мл", "л", "литр", "литров",
    "мм", "см", "м",
    "шт.", "арт", "арт.",
}


def normalize_name(name: str) -> str:
    """
    Нормализует название товара для сопоставления прайса с ассортиментом.

    Шаги:
    1. Unicode → ASCII-подобное (ё→е и т.д.)
    2. Нижний регистр
    3. Убрать всё кроме букв, цифр, пробелов
    4. Убрать стоп-слова
    5. Схлопнуть пробелы, trim

    Примеры:
        "Молоко Простоквашино 3.2% 1л" → "молоко простоквашино 3.2"
        "Сок Добрый яблоко, 1 л" → "сок добрый яблоко 1"
    """
    if not name:
        return ""

    # Нормализуем unicode (ё → е через NFKD не работает, делаем явно)
    text = name.replace("ё", "е").replace("Ё", "е")

    # Нижний регистр
    text = text.lower()

    # Заменяем дефисы и слэши на пробел (чтобы "яблоко-груша" → "яблоко груша")
    text = re.sub(r"[-/\\]", " ", text)

    # Убираем всё кроме букв, цифр, точек (для "3.2%") и пробелов
    text = re.sub(r"[^\w\s.]", " ", text, flags=re.UNICODE)

    # Убираем проценты рядом с числами (3.2% → 3.2)
    text = re.sub(r"(\d+\.?\d*)%", r"\1", text)

    # Разбиваем на токены и фильтруем стоп-слова
    # Одиночные цифры оставляем (версии моделей: "Major 5"), одиночные буквы убираем
    tokens = text.split()
    tokens = [t for t in tokens if t not in _STOP_WORDS and (len(t) > 1 or t.isdigit())]

    return " ".join(tokens)
